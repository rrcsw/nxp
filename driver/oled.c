void delay_1us(unsigned int Del_1ms)		//
{ 
  int i;
  while(Del_1ms--)
  {	
    for(i=0;i<3;i++) asm("NOP");//30000是1ms,300是100K
  }
}
void LCD_WrDat(unsigned char data)
{
  unsigned char i = 8;
  //LCD_CS=0;;
  LCD_DC(1);
  LCD_SCL(0);
  delay_1us(1);	    
  while(i--)
  {
    if(data&0x80)
			LCD_SDA(1);
    else
			LCD_SDA(0);
    LCD_SCL(1);
    delay_1us(1);	          
    LCD_SCL(0);    
    data<<=1;    
  }
  //LCD_CS=1;
}
void LCD_WrCmd(unsigned char cmd)
{
  unsigned char i=8;
  
  //LCD_CS=0;;
  LCD_DC(0);
  LCD_SCL(0);
  delay_1us(1);	  
  while(i--)
  {
    if(cmd&0x80)
			LCD_SDA(1);
    else
			LCD_SDA(0);
    LCD_SCL(1);
    delay_1us(1);	            
    LCD_SCL(0);    
    cmd<<=1;;   
  } 	
  //LCD_CS=1;
}
void LCD_Set_Pos(unsigned char x, unsigned char y)
{ 
  LCD_WrCmd(0xb0+y);
  LCD_WrCmd(((x&0xf0)>>4)|0x10);
  LCD_WrCmd((x&0x0f)); 
} 
void LCD_Fill(unsigned char bmp_data)
{
  unsigned char y,x;
  
  for(y=0;y<8;y++)
  {
    LCD_WrCmd(0xb0+y);
    LCD_WrCmd(0x01);
    LCD_WrCmd(0x10);
    for(x=0;x<X_WIDTH;x++)
      LCD_WrDat(bmp_data);
  }
}
void LCD_CLS(void)
{
  unsigned char y,x;	
  for(y=0;y<8;y++)
  {
    LCD_WrCmd(0xb0+y);
    LCD_WrCmd(0x01);
    LCD_WrCmd(0x10); 
    for(x=0;x<X_WIDTH;x++)
      LCD_WrDat(0);
  }
}

