/**********************************************************
函数名称：TFT_Draw_circle()
入口参数：圆心横坐标(0-127)，纵坐标(0-159),半径(0-128),显示颜色uint16
出口参数：无
时间：2018-01-28
功能说明：画圆形边框(仅支持屏幕内画圆)
其他说明：0<=x<=127
0<=y<=159
**********************************************************/
void TFT_Draw_Circle(uint16_t x,uint16_t y,uint16_t r,uint32_t fcolor,uint8_t clr)
{
  uint16_t dx, dy = r; 
  
  if((x>=r) &&((APP_IMG_WIDTH-x)>=r) && (y>=r) && ((APP_IMG_HEIGHT-y)>=r))		//确定所画圆在屏幕范围内，没有超出最外边，(暂不支持与屏幕边相交)
  {
    for(dx = 0; dx <= r; dx++)
    {
      while((r * r + 1 - dx * dx) < (dy * dy)) dy--;
      TFT_Draw_Dot(x + dx, y - dy, fcolor);
      TFT_Draw_Dot(x - dx, y - dy, fcolor);
      TFT_Draw_Dot(x - dx, y + dy, fcolor);
      TFT_Draw_Dot(x + dx, y + dy, fcolor);
      
      TFT_Draw_Dot(x + dy, y - dx, fcolor);
      TFT_Draw_Dot(x - dy, y - dx, fcolor);
      TFT_Draw_Dot(x - dy, y + dx, fcolor);
      TFT_Draw_Dot(x + dy, y + dx, fcolor);
    }
  }
}
