//液晶字符输出(6*8字体)
//x: 0 - 20	(行)
//y: 0 -19	(列)
void TFT_P6X8(uint16_t x, uint16_t y, uint8_t c_dat, uint32_t fcolor, uint32_t bcolor, uint8_t clr)
{
  uint32_t i,j;  
  
  for(j=0; j<8; j++)
  {
    for(i=0; i<6; i++)
    {
      if((Font_code8[c_dat-32][i]) & (0x01<<j)) //字体颜色       
        TFT_Draw_Dot(x+i, y+j,fcolor);	//画点
      else
      {
        if(clr) TFT_Draw_Dot(x+i, y+j,bcolor);//背景色
        //else    TFT_Draw_Dot(x+i, y+j,bcolor,clr);//
      }
      
    }
  }
}
